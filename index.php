<script>
    const slogans = document.querySelectorAll('.slogan-container span'); // Sloganları seç

    let currentSloganIndex = 0; // Başlangıç sloganı

    function changeSlogan() {
        slogans[currentSloganIndex].classList.remove('active'); // Önceki sloganı gizle
        currentSloganIndex = (currentSloganIndex + 1) % slogans.length; // Sonraki sloganı seç
        slogans[currentSloganIndex].classList.add('active'); // Yeni sloganı göster
    }

    setInterval(changeSlogan, 3000); // Her 3 saniyede bir slogan değiştir
</script>

